import java.util.*;

public class _36UpperLowerAll {
    public static void main(String[] args) {
    	String[] array = {"tHe","QuIcK","BrOWn","FoX","juMps","OVer","THe","LaZy","DOg"};
    	   System.out.println("Original array: " + Arrays.toString(array));
    	   System.out.println("Lowercase array: " + Arrays.toString(array).toLowerCase());
    	   System.out.println("Uppercase array: " + Arrays.toString(array).toUpperCase());
    }
}